class FunnyCircle{
    x;
    y;
    radius;

    constructor(x, y, radius){
        this.x = x;
        this.y = y;
        this.radius = radius;
    }

    getX(){
        return this.x;
    }

    getY(){
        return this.y;
    }

    setX(x){
        this.x = x;
    }

    setY(y){
        this.y = y;
    }
}